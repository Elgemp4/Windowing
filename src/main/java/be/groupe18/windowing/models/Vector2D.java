package be.groupe18.windowing.models;

public class Vector2D {
    private double first;
    private double second;

    public Vector2D(double first, double second) {
        this.first = first;
        this.second = second;
    }

    public double getFirst() {
        return first;
    }

    public double getSecond() {
        return second;
    }
}
